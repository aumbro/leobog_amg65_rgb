FALLBACK = ("111", "101", "101", "101", "111")
SPACE_WIDTH = 2
TRACKING = 1  # ช่องว่างระหว่างตัวอักษร


def glyph(char: str) -> tuple[str, ...]:
    """คืน bitmap ของตัวอักษร (ตัวเล็กถูกยกเป็นตัวใหญ่ ตัวที่ไม่รู้จักได้กล่องทึบ)."""
    if char in GLYPHS:
        return GLYPHS[char]
    upper = char.upper()
    if upper in GLYPHS:
        return GLYPHS[upper]
    return FALLBACK


def char_width(char: str) -> int:
    if char == " ":
        return SPACE_WIDTH
    return len(glyph(char)[0])


def text_width(text: str, tracking: int = TRACKING) -> int:
    """ความกว้างรวมเป็นพิกเซล (ไม่นับ tracking ท้ายสุด)."""
    if not text:
        return 0
    return sum(char_width(c) for c in text) + tracking * (len(text) - 1)


def iter_pixels(text: str, tracking: int = TRACKING):
    """ไล่พิกเซลที่ติดของข้อความ คืน (x, y, ลำดับตัวอักษร) โดยยึด x=0 ที่ตัวแรก."""
    cursor = 0
    for position, char in enumerate(text):
        bitmap = glyph(char)
        for y, row in enumerate(bitmap):
            for dx, bit in enumerate(row):
                if bit == "1":
                    yield cursor + dx, y, position
        cursor += char_width(char) + tracking
